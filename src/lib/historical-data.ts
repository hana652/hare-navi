export function getHistoricalRate(regionCode: string, month: number, day: number): {
  rate: number;
  years: number;
  sunnyDays: number;
  totalDays: number;
} {
  const stats = HISTORICAL_SUNNY_RATES[regionCode];
  if (!stats) {
    return { rate: 50, years: 30, sunnyDays: 15, totalDays: 30 };
  }

  // 月のインデックス(0-based)
  const monthIdx = month - 1;
  const rate = stats.sunnyRate[monthIdx];

  // 概算の日数（30年 × 前後1日で3日 = 90日分のデータ想定）
  const totalDays = 90;
  const sunnyDays = Math.round(totalDays * rate / 100);

  return { rate, years: 30, sunnyDays, totalDays };
}
